// Reverse lookup: emoji char → category name
const EMOJI_TO_CATEGORY = new Map<string, string>();
for (const cat of EMOJI_CATEGORIES) {
  for (const e of cat.emojis) {
    EMOJI_TO_CATEGORY.set(e, cat.name);
  }
}

type EmojiPickerProps = {
  onSelect: (emoji: string) => void;
  onRemove: () => void;
  onClose: () => void;
};

export default function EmojiPicker({ onSelect, onRemove, onClose }: EmojiPickerProps) {
  const [query, setQuery] = useState("");
  const ref = useRef<HTMLDivElement>(null);
  const inputRef = useRef<HTMLInputElement>(null);

  useClickOutside(ref, onClose, true);

  useEffect(() => {
    inputRef.current?.focus();
  }, []);

  // Keyword-powered search, grouped by category
  const searchGrouped = useMemo(() => {
    const q = query.trim();
    if (!q) return null;
    const results = emojiSearch(q).slice(0, 50);
    if (results.length === 0) return [];
    const groups = new Map<string, string[]>();
    for (const r of results) {
      const cat = EMOJI_TO_CATEGORY.get(r.name) ?? "Other";
      const list = groups.get(cat) ?? [];
      list.push(r.name);
      groups.set(cat, list);
    }
    // Preserve category order from EMOJI_CATEGORIES
    const ordered: { name: string; emojis: string[] }[] = [];
    for (const cat of EMOJI_CATEGORIES) {
      const emojis = groups.get(cat.name);
      if (emojis) ordered.push({ name: cat.name, emojis });
    }
    const other = groups.get("Other");
    if (other) ordered.push({ name: "Other", emojis: other });
    return ordered;
  }, [query]);

  const handleSelect = useCallback(
    (emoji: string) => {
      onSelect(emoji);
      onClose();
    },
    [onSelect, onClose],
  );

  const handleRemove = useCallback(() => {
    onRemove();
    onClose();
  }, [onRemove, onClose]);

  const categories = searchGrouped ?? EMOJI_CATEGORIES;

  return (
    <div
      ref={ref}
      className="absolute top-full left-0 mt-1 w-[256px] bg-surface border border-line rounded-lg shadow-xl z-50 flex flex-col overflow-hidden"
      style={{ maxHeight: 320 }}
    >
      <div className="px-1.5 pt-1.5 pb-0.5">
        <input
          ref={inputRef}
          value={query}
          onChange={(e) => setQuery(e.target.value)}
          placeholder="Search emojis..."
          className="w-full px-2 py-1.5 bg-canvas border border-line rounded-md text-sm text-fg outline-none focus:border-accent placeholder:text-fg-muted"
        />
      </div>
      <div className="flex-1 overflow-y-auto px-1 pb-0.5" style={{ maxHeight: 240 }}>
        {categories.length > 0 ? (
          categories.map((cat) => (
            <div key={cat.name}>
              <div className="text-[10px] font-semibold uppercase tracking-wider text-fg-muted px-0.5 pt-1.5 pb-0.5">
                {cat.name}
              </div>
              <div className="grid grid-cols-8">
                {cat.emojis.map((emoji, i) => (
                  <button
                    key={`${cat.name}-${i}`}
                    onClick={() => handleSelect(emoji)}
                    className="w-[28px] h-[28px] flex items-center justify-center rounded text-[20px] hover:bg-hover transition-colors"
                  >
                    {emoji}
                  </button>
                ))}
              </div>
            </div>
          ))
        ) : (
          <div className="text-center py-6 text-fg-muted text-sm">
            No emojis found
          </div>
        )}
      </div>
      <div className="border-t border-line px-1.5 py-1">
        <button
          onClick={handleRemove}
          className="text-xs text-fg-dim hover:text-fg px-1.5 py-1 rounded hover:bg-hover transition-colors"
        >
          Remove icon
        </button>
      </div>
    </div>
  );
}
